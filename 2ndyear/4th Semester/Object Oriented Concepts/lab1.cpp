/**
 * Write a program to print Integer, character, string, float and double value
 * by initalizing them.
 */
#include <iostream>

int main()
{
    int integer = 10;
    char character = 'a';
    std::string str = "Hello";
    float floating = 6.9;
    double double_floating = 44.66;
    
    std::cout<<"Integer: "<<integer<<"\nCharacter: "<<character<<"\nString: "<<str<<"\nFloating: "<<floating<<"\nDouble: "<<double_floating;
    return 0;
}